# Try to enable BigVGAN fused kernels post-init
def _try_enable_bigvgan_fused(pipeline) -> None:
    """Enable BigVGAN fused CUDA kernels if available"""
    try:
        # Look for BigVGAN instances in pipeline
        cand = None
        for attr in ("bigvgan_model", "vocoder", "vits_model"):
            if hasattr(pipeline, attr):
                obj = getattr(pipeline, attr)
                if hasattr(obj, "model"):
                    obj = obj.model
                if hasattr(obj, "remove_weight_norm") or hasattr(obj, "forward"):
                    cand = obj
                    break
        
        if cand is None:
            print("[BIGVGAN] No BigVGAN instance found; skipping fused toggle.")
            return
            
        # Try to enable fused kernels
        for flag in ("use_cuda_kernel", "use_cudakernel", "fused_kernels", "enable_cuda_kernel"):
            if hasattr(cand, flag):
                try:
                    setattr(cand, flag, True)
                    print(f"[BIGVGAN] Enabled fused CUDA kernels via '{flag}'.")
                    return
                except Exception as e:
                    print(f"[BIGVGAN] Failed setting '{flag}': {e}")
        
        print("[BIGVGAN] Fused toggle not exposed; relying on env BIGVGAN_USE_CUDA_KERNEL=1.")
    except Exception as e:
        print(f"[BIGVGAN] Error enabling fused kernels: {e}")

_try_enable_bigvgan_fused(tts_pipeline)

